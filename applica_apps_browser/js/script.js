document.querySelectorAll(".apps-cont a").forEach((btn) =>
	btn.addEventListener("click", (e) => {
		e.preventDefault();
		activateAppWindow(Number(btn.getAttribute("num")));
	})
);

document
	.querySelector(".close_btn")
	.addEventListener("click", (_) =>
		document.querySelector(".app_window_container").classList.remove("active")
	);

function activateAppWindow(app_num) {
	document.querySelector(".app_window_container .app_logo img").src =
		image_srcs[app_num];
	document.querySelector(".app_window_container .app_logo span").innerText =
		span_texts[app_num];

	document.querySelector(".app_window_container .app_btns a.demo_view").href =
		hrefs[app_num];

	document.querySelector(".app_window_container .demo_display video").src =
		app_videos[app_num];

	document.querySelector(".app_window_container").classList.add("active");
}
